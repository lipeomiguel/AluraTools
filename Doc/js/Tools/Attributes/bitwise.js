/**
 * * BitWise Operadores Binários.
 * ! Operador de negação [ & ];
 * ! Operador de negação [ | ];
 * ! Operador de negação [ ^ ];
 * ! Operador de Bit [ << ];
 * ! Operador de Bit [ >> ];
 */
let nOne = 14;
let nTwo = 10;
console.log(nOne & nTwo);
console.log(nOne | nTwo);
console.log(nOne ^ nTwo);
console.log(nOne >>1);
console.log(nOne <<1);